$min1=$arr2[0][0];//старта за сравняване за най-малко в 1ва
$min2=$arr2[1][1];//старта за сравняване за най-малко в 2ра
$min3=$arr2[2][1];//старта за сравняване за най-малко в 3та
$min4=$arr2[3][1];//старта за сравняване за най-малко в 4та
