for ($y=0; $y < $n; $y++) { //външен

	echo "<tr>";//отваряне на редове
	for ($q=0; $q < $m; $q++) { //вътрешен
		echo "<td>".$arr2[$q][$y]."</td>";//датата в клетките

		for ($f=$y; $f < 1; $f++) { //най-малко в 1ва колона
			if ($arr2[$f][$y] < $min1) {
				$min1=$arr2[$f][$y];
			}
		}
		for ($h=1; $h < 2; $h++) { //най-малко в 2ра колона
			if ($arr2[$h][$y] < $min2) {
				$min2=$arr2[$h][$y];
			}
		}
		for ($z=2; $z < 3; $z++) { //най-малко в 3та колона
			if ($arr2[$z][$y] < $min3) {
				$min3=$arr2[$z][$y];
			}
		}
		for ($l=3; $l < 4; $l++) { //най-малко в 4та колона
